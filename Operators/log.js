// Logical Operators

// AND (&&) Dono condition true hone chahiye.
let age = 20 ;
let hasID = true;
console.log(age >= 18 && hasID);

// OR (||) ek bhi condition true hoge to result true milega.
console.log(true || false);

// NOT (!) Result ulta kar deta hai.
console.log(!true);


// Real life example:-
let usreName = "Admin";
let passsword = "1223";
console.log(
    usreName === "Admin" &&
    passsword === "1223"
);
