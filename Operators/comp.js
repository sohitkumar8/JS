// Comparison Operators

// == (Equal to) sirf value compare karta hai
console.log(5 == "5");

// === (Strict Equal) value + type done compare karta hai.
console.log(5 === "5");

// != Not Equal
console.log(10 != 5);

// !== Strict Not Equal
console.log(10 !== 10);

// Greater Than (>)
console.log(20 > 10);

// Less Than (<)
console.log(10 < 20);

// >= 
console.log(10 >= 10);

// <= 
console.log(10 <= 20);


// Real life project 

let age = 18;
console.log(age >= 18);
