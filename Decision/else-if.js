/*
    else if Ladder

    Jab multiple condition check karna ho.
*/

let marks = 75;

if(marks >= 90){
    console.log("A Grade");
    
}else if(marks >= 80){
    console.log("B Grade");
    
}else if(marks >= 70){
    console.log("C Grade");
    
}else{
    console.log("FAIL");
    
}

