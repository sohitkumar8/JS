let fruits = ["Apple", "Banana", "Mango"];
console.log(fruits);


//  1. Value Change karna
fruits[0] = "Orange";
console.log(fruits);


//  2.Array length 
console.log(fruits.length);


//  3. Array me Add karna
// push() last me add krta hai.
fruits.push("Apple");
console.log(fruits);

// unshift() start me add 
fruits.unshift("Grapes");
console.log(fruits);


//  4.Array se remove
// pop() last remove
fruits.pop();
console.log(fruits);

// shift start remove
fruits.shift();
console.log(fruits);



